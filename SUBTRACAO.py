#Faça um programa que faça a diferença de 2 notas de alunos e imprima na tela

nota1 = int(input("\nDigite a 1ª nota: "))
nota2 = int(input("\nDigite a 2ª nota: "))

total = nota1 - nota2

print(f'\nA diferença da subtração da nota1 {nota1} e da nota 2 {nota2} é = ',total)
print('')

# O \n  é para saltar uma linha
# O print('') é para saltar uma linha no final do código
